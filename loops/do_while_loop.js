/* Create a do...while loop which increments cupsAdded
   by one while cupsAdded is less than cupsOfSugarNeeded. */

   const cupsOfSugarNeeded = 3;
   let cupsAdded = 0;
   
   do {
     cupsAdded++;
     console.log(cupsAdded);
   } while (cupsAdded < cupsOfSugarNeeded);
   